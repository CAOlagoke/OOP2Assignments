import java.util.ArrayList;

public class UserValidator{

    private ArrayList<Validateable> validations;

    public UserValidator(ArrayList<Validateable> validations) {
        this.validations = this.validations;
    }

    public ArrayList<Validateable> getValidations() {
        return validations;
    }

    public void setValidations(ArrayList<Validateable> validations) {
        this.validations = validations;
    }



}
